%--- Wilcoxon (ranksum) between all unique pairs, on col 1 ---
pairs = nchoosek(1:4,2);
for k = 1:size(pairs,1)
    i = pairs(k,1); j = pairs(k,2);
    [P,H,ST] = ranksum(Algs{i}(:,1), Algs{j}(:,1));
    fprintf('ranksum %s vs %s: P=%.3g, H=%d\n', labels{i}, labels{j}, P, H);
end

%--- Friedman test on col 1 for all pairs ---
for k = 1:size(pairs,1)
    i = pairs(k,1); j = pairs(k,2);
    % data must be observations×methods, so transpose and stack
    D = [Algs{i}(:,1)'; Algs{j}(:,1)'];
    [P,T,ST] = friedman(D);
    fprintf('friedman %s vs %s: P=%.3g\n', labels{i}, labels{j}, P);
end

