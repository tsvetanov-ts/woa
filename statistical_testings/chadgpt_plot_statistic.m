    i = pairs(k,1); j = pairs(k,2);
    [P,H,ST] = ranksum(Algs{i}(:,1), Algs{j}(:,1));
    fprintf('ranksum %s vs %s: P=%.3g, H=%d\n', labels{i}, labels{j}, P, H);
